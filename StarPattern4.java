package JavaBasic;

public class StarPattern4 {
	

	public static void main(String[] args) {
		
	
	int row=5;
	
	for(int i=0;i<=row;i++) {
		
		;
		for(int j=row;j>=i*2;j--) {
			
			
			System.out.print("*");
			
		}
		
		
		
		System.out.println(" ");
		
		
	}
	
	
		
	}
	

}
