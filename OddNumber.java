package JavaBasic;

public class OddNumber {

	public static void main(String[] args) {
		
		int i;
		
		int a=0;
		
		
		//even numbers from 1 to 100
		
		System.out.println("even numbers form 1 to 100");
		for (i=0;i<100;i++) {
			
			
			if (i%2==0)
			      {
				    
				   a++;
				  
				   System.out.println("even no ="+i);	
			      
	
			      }
			
			
		     }
		
		System.out.println("total even numbers ="+a);
		
		
		
}

}
